    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //botao excluir
        if (jTableRelatorio.getSelectedRow() != -1){

            Manutencao it = new Manutencao();
            ManutencaoDAO dao = new ManutencaoDAO();
            
            Manutencao.setChaveID((int) jTableRelatorio.getValueAt(jTableRelatorio.getSelectedRow(), 0));
            
            dao.delete(it);
            
            readJTable();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma Manutenção!");
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

