    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableRelatorio.getModel();
        modelo.setNumRows(0);
        ManutencaoDAO mdao = new ManutencaoDAO();
        String nomeIt = null;
        String nomeMaq = null;
        for(Manutencao it: mdao.read()){
            modelo.addRow(new Object[]{
                it.getIdManutencao(),
                it.getDia(),
                nomeIt = mdao.readNomeItem(it),
                nomeMaq = mdao.readNomeMaq(it),
                it.getHoras_dur(),
                String.format("%.2f",it.getPreco()),
                it.getMarca(),
            });
        }
    }

