    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableMaq.getModel();
        int i = 1;
        modelo.setNumRows(0);
        MaquinaDAO mdao = new MaquinaDAO();
        
        for(Maquina m: mdao.read()){
            modelo.addRow(new Object[]{
                m.getIdMaquina(),
                i,
                m.getNome(),
            });
            i++;
        }
    }

